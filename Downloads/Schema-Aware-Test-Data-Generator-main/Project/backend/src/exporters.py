def export_report(
    schema: SchemaModel,
    all_data: Dict[str, List[Dict[str, Any]]],
    generation_order: List[str],
    validation_passed: bool,
    validation_issues: List[str],
    agent_log: str,
    output_path: str,
) -> str:
    """
    Write a Markdown generation report to output_path.
    Returns the report content as a string.
    """
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    lines = [
        "# Schema-Aware Test Data Generator — Generation Report",
        f"\n**Generated:** {ts}\n",
        "---",
        "",
        "## 1. Tables Detected",
        "",
    ]

    for tname, table in schema.tables.items():
        pks = ", ".join(table.get_primary_keys()) or "none"
        fks = ", ".join(str(fk) for fk in table.foreign_keys) or "none"
        lines.append(f"### `{tname}`")
        lines.append(f"- **Columns ({len(table.columns)}):** "
                     f"{', '.join(c.name for c in table.columns)}")
        lines.append(f"- **Primary Keys:** {pks}")
        lines.append(f"- **Foreign Keys:** {fks}")
        lines.append(f"- **Rows Generated:** {len(all_data.get(tname, []))}")
        lines.append("")

    lines += [
        "---",
        "",
        "## 2. Generation Order",
        "",
        " -> ".join(generation_order),
        "",
        "---",
        "",
        "## 3. Validation Result",
        "",
    ]

    if validation_passed:
        lines.append("✅ **All validation checks PASSED.**")
    else:
        lines.append(f"⚠️ **{len(validation_issues)} validation issue(s) detected:**")
        for issue in validation_issues:
            lines.append(f"- {issue}")

    lines += [
        "",
        "---",
        "",
        "## 4. Agent Loop Log",
        "",
        "```",
        agent_log,
        "```",
        "",
        "---",
        "",
        "## 5. Assumptions & Limitations",
        "",
        "- Supports common SQL DDL patterns only (MySQL/SQLite/PostgreSQL-style).",
        "- Composite primary keys are partially supported.",
        "- Cyclic foreign key relationships are NOT supported.",
        "- Complex CHECK constraints, triggers, and stored procedures are out of scope.",
        "- NULL probability is fixed at ~12% for nullable columns.",
        "- Data is synthetic — not suitable for production use.",
        "",
        "---",
        "*Generated by Schema-Aware Test Data Generator — Infinite Computer Solutions Tech Challenge*",
    ]

    report_content = "\n".join(lines)
    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(report_content)

    return report_content


def get_report_bytes(
    schema: SchemaModel,
    all_data: Dict[str, List[Dict[str, Any]]],
    generation_order: List[str],
    validation_passed: bool,
    validation_issues: List[str],
    agent_log: str,
) -> bytes:
    """Return the report as bytes (for Streamlit download button)."""
    content = export_report(
        schema, all_data, generation_order,
        validation_passed, validation_issues, agent_log,
        output_path="/dev/null" if os.name != "nt" else "NUL",
    )
    return content.encode("utf-8")
